data=read.table("household_power_consumption.txt",header=TRUE,sep=";",na.strings="?")
data$Date=as.Date(data$Date,"%d/%m/%Y")
x=which(data$Date%in%c(as.Date("2007-02-01"),as.Date("2007-02-02")))
usedata=data[x,]
usedata$Weekday=weekdays(usedata$Date)
par(mfrow=c(2,2))
source("plot2.R")
plot(usedata$Voltage,type="l",axes=F,xlab="datetime",ylab="Voltage")
axis(2)
axis(1,at=c(1,min(which(usedata$Weekday=="星期五")),length(usedata[,1])),labels=c("Thu","Fri","Sat"))
source("plot3.R")
plot(usedata$Global_reactive_power,type="l",axes=F,xlab="datetime",ylab="Global_reactive_power")
axis(2)
axis(1,at=c(1,min(which(usedata$Weekday=="星期五")),length(usedata[,1])),labels=c("Thu","Fri","Sat"))
